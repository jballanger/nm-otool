#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int		i;
	char	*copy;

	copy = (char*)s;
	i = 0;
	if (n > 0)
	{
		while ((size_t)i < n)
		{
			*copy = '\0';
			copy++;
			i++;
		}
	}
}
